package folder.HW4;

public class Task_4_1 {
    public static void main(String[] args) {
        {
            System.out.println("FOR cycle");
        }
        //for cycle
        for (int i = 10; i <= 20; i++) {
            System.out.print(i + " ");
        }
        {
            System.out.println(" ");
            System.out.println("WHILE cycle");
        }
        //while cycle
        int i = 10;
        while (i <= 20) {
            System.out.print(i + " ");
            i++;
        }
        {
            System.out.println(" ");
            System.out.println("DO-WHILE cycle");
        }
        {
         //DO_WHILE
            int a = 10;
            do {
                System.out.print(a + " ");
                a++;
            } while (a <= 20);
        }
        {
            System.out.println(" ");
            System.out.println("FOREACH cycle");
        }
        //foreach cycle

            }
    }



